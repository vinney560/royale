HEADERS = random.choice(ALL_HEADERS)

def get_live_video_id(channel_id):
    """
    Get the current live video ID from a YouTube channel.
    
    Args:
        channel_id (str): YouTube channel ID (e.g., 'UCKVsdeoHExltrWMuK0hOWmg')
        
    Returns:
        str: Live video ID, or None if not found
    """
    url = f"https://www.youtube.com/channel/{channel_id}/live"
    print(f"Fetching: {url}")
    
    try:
        response = requests.get(url, headers=HEADERS, timeout=10, allow_redirects=True)
        print(f"Status code: {response.status_code}")
        print(f"Redirect location: {response.headers.get('Location', 'None')}")
        
        if response.status_code == 302 or response.status_code == 301:
            location = response.headers.get('Location', '')
            print(f"Redirect URL: {location}")
            if 'watch?v=' in location:
                match = re.search(r'watch\?v=([^&]+)', location)
                if match:
                    video_id = match.group(1)
                    print(f"Found video ID from redirect: {video_id}")
                    return f"https://www.youtube.com/embed/{video_id}"
        
        if response.status_code == 200:
            print("No redirect, checking page content...")
            html = response.text
            match = re.search(r'"videoId"\s*:\s*"([^"]+)"', html)
            if match:
                video_id = match.group(1)
                print(f"Found video ID in HTML: {video_id}")
                return f"https://www.youtube.com/embed/{video_id}"
        
        print("No video ID found")
        return None
        
    except Exception as e:
        print(f"Error: {e}")
        return None